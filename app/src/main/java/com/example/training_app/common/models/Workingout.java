package com.example.training_app.common.models;

public class Workingout {

    private int allCalories;
    private int currentCalories;
    private int allCards;
    private int currentCards;
    private int allStrengths;
    private int currentStrengths;
    private int allAgilitys;
    private int currentAgilitys;

    public int getAllCaloriesWorkingout() {
        return allCalories;
    }

    public void setAllCaloriesWorkingout(int calories) {
        this.allCalories = calories;
    }

    public int getCurrentCaloriesWorkingout() {
        return currentCalories;
    }

    public void setCurrentCaloriesWorkingout(int calories) {
        this.currentCalories = calories;
    }

    public int getAllCardsWorkingout() {
        return allCards;
    }

    public void setAllCardsWorkingout(int cards) {
        this.allCards = cards;
    }

    public int getCurrentCardsWorkingout() {
        return currentCards;
    }

    public void setCurrentCardsWorkingout(int cards) {
        this.currentCards = cards;
    }

    public int getAllStrengthsWorkingout() {
        return allStrengths;
    }

    public void setAllStrengthsWorkingout(int strengths) {
        this.allStrengths = strengths;
    }

    public int getCurrentStrengthsWorkingout() {
        return currentStrengths;
    }

    public void setCurrentStrengthsWorkingout(int strengths) {
        this.currentStrengths = strengths;
    }

    public int getAllAgilitysWorkingout() {
        return allAgilitys;
    }

    public void setAllAgilitysWorkingout(int agilitys) {
        this.allAgilitys = agilitys;
    }

    public int getCurrentAgilitysWorkingout() {
        return currentAgilitys;
    }

    public void setCurrentAgilitysWorkingout(int agilitys) {
        this.currentAgilitys = agilitys;
    }
}
